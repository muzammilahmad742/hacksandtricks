VPN (Virtual Private Network) technology lets a computer using a public 
Internet connection join a private network by way of a secure “tunnel” 
between that machine and the network. The most common case is a business
 allowing its employees to connect to its work network from home or from
 the road.<br />
<br />
There are two principal ways to configure VPN. The first and more-common scenario, called <i>outgoing,</i> is setting up a remote computer to call into the office network. The second scenario, called <i>incoming,</i>
 occurs on the network side, where a computer allows secure connections 
from other computers. Windows 7 comes preloaded with the Agile VPN 
client, which makes setting up either kind of connection relatively 
easy.<br />
<br />
<h3 class="subhed">
Step by Step: Connecting to a VPN (Outgoing)</h3>
<b>Step 1</b> Click the <i>Start</i> button. In the search bar, type <b>VPN</b> and then select <i>Set up a virtual private network (VPN) connection</i>.<br />
<br />
<b>Step 2</b> Enter the IP address or domain name of the 
server to which you want to connect. If you’re connecting to a work 
network, your IT administrator can provide the best address.<br />
<br />
<b>Step 3</b> If you want to set up the connection, but not connnect, select <i>Don’t connect now</i>; otherwise, leave it blank and click <i>Next</i>.<br />
<br />
<b>Step 4</b> On this next screen, you can either put in 
your username and password, or leave it blank. You’ll be prompted for it
 again on the actual connection. Click <i>Connect</i>.<br />
<br />
<b>Step 5</b> To connect, click on the Windows network logo on the lower-right part of your screen; then select <i>Connect</i> under VPN Connection.<br />
<br />
<b>Step 6</b> In the Connect VPN Connection box, enter the appropriate domain and your log-in credentials; then click <i>Connect</i>.<br />
<br />
<b>Step 7</b> If you can’t connect, the problem could be 
due to the server configuration. (There are different types of VPN.) 
Check with your network administrator to see what kind is in use–such as
 PPTP–then, on the Connect VPN Connection screen, select <i>Properties</i>.<br />
<br />
<b>Step 8</b> Navigate to the Security tab and select the specific Type of VPN from the drop-down list. You may also have to unselect <i>Include Windows logon domain</i>under the Options tab. Then click <i>OK</i> and<i>Connect</i>.<br />
<br />
<br />
<h3 class="subhed">
Step by Step: Building a VPN (Incoming)</h3>
<b>Step 1</b> Click the <i>Start</i> button, and, in the search bar, type <b>Network and Sharing</b>.<br />
<br />
<b>Step 2</b> Click <i>Change Adapter Settings</i> in the left-hand menu.<br />
&nbsp; <br />
<b>Step 3</b> Click <i>File</i>, and then <i>New Incoming Connection</i>.<br />
<br />
<b>Step 4</b> Select the users you’d like to give access to and click <i>Next</i>.<br />
<br />
<b>Step 5</b> Click <i>Through the Internet </i>and select <i>Next</i>.<br />
<br />
<b>Step 6</b> Select the Internet Protocol you’d like to 
use. (The default TCP/IPv4–the line highlighted in the screenshot 
below–will work fine.)<br />
<br />
<b>Step 7</b> Finally, click <i>Allow access</i>; you’ve now set up an incoming VPN connection.

