<h3 class="text-center">How To Protect External Hard Disk Drives With Password</h3>

	<br/><br/>
<br>Time to protect your external Hard Drive with encryption for securing your data from unauthorized access. We are going to share two methods that will help you protect your External Hard Disk Drives with the password. Go through the post to know about it.<br/>
0.
Security is always the first priority for every user and when it’s about some personal data then it becomes more important. If we talk about external hard drive disk where you store lots of personal data is not at all secure, because anyone can carry your portable drive and can do some illegal act. However, it can be password protected, you can set your desired password to unlock the external drive on any computer. So have a look at complete guide discussed below to protect your external drive with a security password.
<br/><br/>
We have mentioned two methods below, one is based on system settings that will allow you to lock your drive with password without any need of third party tool. Whereas, the other one requires a third party tool to password-protect your drives. Just follow up some simple steps below to proceed.
<br/><br/>
Steps To Secure Your External Hard Drive With BitLocker:</b>
<br/><br/>
<strong>Step 1.</strong> First of all, connect your external hard drive to your Windows PC and turn it on. Now click on Start button and then navigate to Control Panel -> BitLocker Drive Encryption.
<br/><br/>
<strong>Step 2.</strong> By default, BitLocker will be “Off” you need to Turn On BitLocker for the drive that you want to encrypt.
<br/><br/>
<strong>Step 3.</strong> Now their tick on “Use a Password to Unlock the Drive” option there and enter the same password in both fields and then click “Next“.
<br/><br/>
<strong>Step 4.</strong> Now time to save your key somewhere for that click on “Save the Recovery Key to a File,” and then select any location where you want to save your key for future reference and then proceed by clicking on next button.
<br/><br/>
<strong>Step 5.</strong> Now click on Start Encryption option and wait for the process to complete and it can take a time to complete depending on drive size. Once finished.
<br/><br/>
That’s it! you are done, now you drive will always a require a password to access files inside it.
Steps To Password Protect Your External Drive With StorageCrypt
<br/><br/>
<strong>Step 1.</strong> First of all in your windows PC download and install the tool StorageCrypt. Now launch the tool and then connect your drive to PC.
<br/><br/>
<strong>Step 2.</strong> Now in the tool, select your device to encrypt with password. Now select quick mode and then protect your drive with your desired password and then click on  “encrypt”
