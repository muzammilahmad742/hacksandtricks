<h3 class="text-center">Does your Wi-Fi feel slow? Here’s how you can fix it</h3><br/><br/>

<center><img src="images/how-to-speed-up-your-home-wifi-.webp"></center><br/><br/>


Did you just splurge on a high-speed internet connection only to find 
that the Wi-Fi speeds seem abysmally slow or do you see frequent 
disconnects on devices that are connected to the network? There are a 
variety of reasons why this could be happening but one of the most 
common ones must do with Wi-Fi channels.<br />
<br />
he way your router works is by taking your Wi-Fi connection and 
assigning a unique channel to it, depending on your router, these are 
normally auto selected, meaning your router jumps from channel to 
channel frequently hopping on to the one it thinks is the least crowded.
 Unfortunately, any other routers in your immediate vicinity are also 
doing the same thing meaning often, your neighbour might just end up 
using the same channel as your router.<br />
<br />
<br />
Luckily, there is an easy fix for it. Before fixing the problem, you 
must identify the channel your router is currently on, depending on your
 system there are a few ways you can do this.<br />
<br />
<b>On a Mac/iOS</b><br />
<br />
On a Mac, hold down the option key and click the Wi-Fi icon on the 
Menu bar. Select Wireless diagnostics. Ignore the setup wizard that 
opens, instead, click on Window and select Scan. In the Window that 
opens, press Scan Now at the bottom. In the left side of the window 
pane, Mac will recommend free channels depending on your frequency, 
write these down.<br />
On an iPhone/iPad, head to the app store and download the AirPort 
Utility App. Head to settings, find AirPort Utility and enable Wi-Fi 
Scanner. Now Scan networks around you and make a note of the channels 
with the most number of networks on them.<br />
<br />
<b>On Windows/Android</b><br />
<br />
On Windows, you are going to need to download a separate utility such
 as Acrylic Wi-Fi. Once you have downloaded the utility, run it and Scan
 for networks, as before making note of the most popular ones.<br />
<br />
On an Android Phone/Tablet, head to the Play store and download Wi-Fi
 Analyzer. Open the app and as before, Scan networks and make note of 
the popular channels.<br />
<br />
<b>Fixing the problem</b><br />
<br />
We are going to need access to your Router’s admin page to make the 
changes, the most common URL to access the settings page is 192.168.1.1,
 input that in your web browser. If nothing happens, then refer to the 
manual that comes with your router for the correct address.<br />
Now you are going to find the Wi-Fi settings page, once there, look 
for the Channels options and select the channel that is being used the 
least, save your settings and exit.<br />
That’s It! You’ve now successfully decoupled your Wi-Fi from a 
crowded channel, this should hopefully bring you some much-needed 
consistency in your home network.<br />
