	<h3 class="text-center">How to un-Google your Android smartphone</h3><br/><br/>



<div dir="ltr" style="text-align: left;" trbidi="on">
Google is at the heart of every Android smartphone. An Android device 
comes bundled with&nbsp;a bunch of&nbsp;Google’s cloud&nbsp;services, to get you 
started. However, in the past few years&nbsp;as&nbsp;Android&nbsp;has evolved our 
dependence on its services have also increased.<br />
<br />
Today, most people use Gmail for their emails, Google Suite for their 
docs and spreadsheets, the Play store for their app and among all these 
services there are two constants — one being that they’re created by 
Google, and secondly, they are synced with your Gmail account.<br />
<br />
Google is also learning your traits using its Google Play Services, and 
it shows you content you’d be interested in on the Play Store. This 
surely sounds like a good thing; however, this also means that the brand
 is looking into all that you do with your phone or browse for, on your 
phone, which also sounds creepy.<br />
<br />
So, if you want to get rid of Google keeping a track of your device, 
here’s how you can remove Google’s omnipresence from your digital life. 
However, you’ll have to keep in mind that the moment you remove these 
services, you won’t be able to use the Play Store to download apps.<br />
<br />
That at being said, don’t worry, we’ve got a few third-party app stores 
to somewhat compensate for it. We’ll get to that later. For now, let’s 
work on getting rid of Google.<br />
<br />
<b>Disabling Apps</b><br />
<br />
Google apps come pre-installed on a smartphone, and unlike other apps, 
you cannot uninstall them like. The only way you can turn a preloaded 
app off is by disabling them. Go to Settings&gt; Apps and manually look 
for all the Google Apps and disable it. All apps might get disabled, 
except one — Google Search.<br />
<br />
Rooting your smartphone is another option which will result in the 
deletion of all the apps. However, this is not only a tricky process but
 will also void your warranty. We recommend the easier solution, but if 
you’re not afraid of taking chances with your phone then root away!<br />
<br />
<b>Adding Apps</b><br />
<br />
With all Google Apps disabled, you’ll need a bunch of alternatives to 
get work done. But the very first app that you’ll need is an app store 
to get apps from. Since Play Store is already disabled, you’ll have to 
look towards alternatives. Some of these stores will also provide access
 to some popular apps.<br />
<br />
<b>9Apps</b><br />
<br />
9Apps is a third party app store that offers many popular apps to choose
 from. It has apps like WhatsApp, and Hike and a bunch of other apps 
which are available on the Play Store. 9Apps is your closest alternative
 to apps available on the Play Store.&nbsp;<br />
<br/>
<strong>Drawbacks for going off-Google</strong> </div>

One of the biggest drawbacks of taking Google out of your Android smartphone is security. Yes, it is understandable that with Google keeping an eye on everything you do is affecting your privacy but when you choose to get apps from third-party App developers or are directly loading apk files for apps, the chances of malware entering your phone are higher.


Another drawback is obviously the ecosystem. You’ve used Android for a while now, and it is really convenient to see all your files and data synced on to one Gmail account, and by simply signing in, you can get the same data on any other device that you switch to. With the elimination of Google services, switching devices will be a task. Additionally, you’ll need to manually backup all your files instead of auto-backup on Google Drive, which can be a pain

At the end of it all, you need to ask yourself, whether you really need that amount of privacy to un-Google your smartphone. And if you really do, these methods would surely come in handy!
